void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)		
{
	if(huart->Instance==USART1){			//USB通信
		RxData1[USART1_RX_STA] = RxBuffer1[0];
		/* 判断第1字节是否正确 */
		if(USART1_RX_STA == 0){
			if(RxData1[0] == 0x57){
				RecState = 0;
			}else if(RxData1[0] == 0xFE){
				RecState = 1;
			}else{
				USART1_RX_STA = 0;
				//memset(RxData1,0,100);
				return;
			}
		}
		
		if(USART1_RX_STA == 1 && RecState == 1){
			data_cnt1 = RxData1[USART1_RX_STA] + 2;	//接收第2字节为包中数据部分长度
		}
		if(USART1_RX_STA == 4 && RecState == 0){
			data_cnt1 = RxData1[USART1_RX_STA] + 1;
		}
		USART1_RX_STA ++;
		if(data_cnt1 != 0){
			data_cnt1 --;
			if(data_cnt1 == 0){
				Handler(RxData1);
				//memset(RxData1,0,100);
				USART1_RX_STA = 0;
			}
		}
	}
/******************************************************************************/
	if(huart->Instance==USART2)		//指纹模块通信
	{
		RxData2[USART2_RX_STA] = RxBuffer2[0];
		
		if(USART2_RX_STA==8 && RxData2[8]!=0)
		{
			data_cnt2 = RxData2[8]+1;
		}
		
		USART2_RX_STA++;
		
		if(data_cnt2 != 0)
		{
			data_cnt2--;
			if(data_cnt2 == 0)
			{
				RxState = 1;
				USART2_RX_STA = 0;
			}
		}
		
		
		if(RxData2[0]!=0xEF || USART2_RX_STA>99)
		{
			USART2_RX_STA=0;
		}
		
		

	}
}
